package com.assignment.beans;


public class Microphone {
    private String micColor;
    private double micSize;
    private String micOwner;

    public String getMicColor() {
        return micColor;
    }

    public void setMicColor(String micColor) {
        this.micColor = micColor;
    }

    public double getMicSize() {
        return micSize;
    }

    public void setMicSize(double micSize) {
        this.micSize = micSize;
    }

    public String getMicOwner() {
        return micOwner;
    }

    public void setMicOwner(String micOwner) {
        this.micOwner = micOwner;
    }
}

